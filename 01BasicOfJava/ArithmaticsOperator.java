public class ArithmaticsOperator {
    public static void main(String[] args) {
        // Sum = a+b // + is operator and rest symbol written are variable
        // LHS =RHS // RHS side is generally take as input
        // LHS is output

        // Flow of Program

        // Variable = Input
        int a=234567;
        int b=2345678;

        // Process 
        int sum= a+b;
        int Multiply= a*b;
        int substraction= a-b;
        int divide= b/a; // Quotient
        int modulus= b%a; // reminder

        // Output

        System.out.println(sum);
        System.out.println(Multiply);
        System.out.println(substraction);
        System.out.println(divide);
        System.out.println(modulus);


        // Why we are using ArithmaticsOperator?
        // Arithmatic is used to solve expression( BODMAS equation)





    }
    
}
