package condtionalStatement;

public class ConditionOperator {
    public static void main(String[] args) {
        // condtion operator is used to decide that the equation is true or false
        // Like LHS = RHS here = is condtion operator in mathematics
        // but in language 
        // > - Greater than
        // < - Less than
        // >= Greater than equal to
        // <= Less than equal to
        // == - Equal to // Its similar to = in mathematics
        // !=  - Not Equal

        int a =45;
        int b =67;

        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a==b);

    }
    
}
